/**
 * Get translated string for key. Lang is 'en' or 'zh'.
 * Use {place} in the string and pass replacements as second arg, e.g. t('weekDay', lang, { week: 1, day: 2 })
 */
export function t(key, lang, replacements = {}) {
  const langKey = lang === 'en' ? 'en' : 'zh';
  let str = translations[langKey][key];
  if (str == null) str = translations.zh[key] || key;
  Object.keys(replacements).forEach((k) => {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(replacements[k]));
  });
  return str;
}

export default { t, translations };
